@section('scripts')
    <script>
        $(document).ready(function() {
            var rutaActual = window.location.href;

            // grafica
            $.ajax({
                url: rutaActual + '/grafica',
                type: 'GET',
                dataType: 'json',
                success: function(data) {
                    grafica(data)
                },
                error: function(error) {
                    Swal.fire({
                        title: "Error",
                        text: "Error al obtener los datos de la gr√°fica.",
                        icon: "error",
                        confirmButtonText: "Aceptar"
                    });
                }
            });

        });

        function grafica(datos) {
            var ctx1 = document.getElementById("chart-line").getContext("2d");
            var gradientStroke1 = ctx1.createLinearGradient(0, 230, 0, 50);

            gradientStroke1.addColorStop(1, 'rgba(94, 114, 228, 0.2)');
            gradientStroke1.addColorStop(0.2, 'rgba(94, 114, 228, 0.0)');
            gradientStroke1.addColorStop(0, 'rgba(94, 114, 228, 0)');

            new Chart(ctx1, {
                type: "line",
                data: {
                    labels: datos.label,
                    datasets: [{
                        label: "",
                        tension: 0.4,
                        borderWidth: 0,
                        pointRadius: 0,
                        borderColor: "#2E7305",
                        backgroundColor: gradientStroke1,
                        borderWidth: 3,
                        fill: true,
                        data: datos.data,
                        maxBarThickness: 6

                    }],
                },
                options: {
                    responsive: true,
                    maintainAspectRatio: false,
                    plugins: {
                        legend: {
                            display: false,
                        }
                    },
                    interaction: {
                        intersect: false,
                        mode: 'index',
                    },
                    scales: {
                        y: {
                            grid: {
                                drawBorder: false,
                                display: true,
                                drawOnChartArea: true,
                                drawTicks: false,
                                borderDash: [5, 5]
                            },
                            ticks: {
                                display: true,
                                padding: 10,
                                color: '#0F0F0F',
                                font: {
                                    size: 11,
                                    family: "Open Sans",
                                    style: 'normal',
                                    lineHeight: 2
                                },
                            }
                        },
                        x: {
                            grid: {
                                drawBorder: false,
                                display: false,
                                drawOnChartArea: false,
                                drawTicks: false,
                                borderDash: [5, 5]
                            },
                            ticks: {
                                display: true,
                                color: '#565656',
                                padding: 20,
                                font: {
                                    size: 11,
                                    family: "Open Sans",
                                    style: 'normal',
                                    lineHeight: 2
                                },
                            }
                        },
                    },
                },
            });
        }
    </script>
